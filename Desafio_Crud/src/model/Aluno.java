package model;

public class Aluno {

	private int aluno_id;
	private String aluno_nome;
	private int aluno_idade;
	public int getAluno_id() {
		return aluno_id;
	}
	public void setAluno_id(int aluno_id) {
		this.aluno_id = aluno_id;
	}
	public String getAluno_nome() {
		return aluno_nome;
	}
	public void setAluno_nome(String aluno_nome) {
		this.aluno_nome = aluno_nome;
	}
	public int getAluno_idade() {
		return aluno_idade;
	}
	public void setAluno_idade(int aluno_idade) {
		this.aluno_idade = aluno_idade;
	}
	
}
